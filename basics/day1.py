# Day 1 - Python Basics (Print & Strings)

# Simple print
print("Hello Jeet")

# Printing my name
print("My name is Jitendra")

# Printing multiple lines
print("Welcome")
print("Day 1 starts here..")

# Using variables (string)
name = "Jitendra"
country = "India"

print("My name is", name)
print("I live in", country)

# String formatting
print(f"My name is {name} and I live in {country}")

# Special characters
print("Hello\nWorld")   # new line
print("Hello\tWorld")   # tab space
